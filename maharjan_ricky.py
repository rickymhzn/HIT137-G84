#function to draw circle with size and color
def draw_circle(size,color):   
    t.pendown()
    t.color(color)
    t.fillcolor(color)
    t.begin_fill()
    t.circle(size)
    t.end_fill()
    t.penup()
#function to draw triangle with size and color    
def draw_triangle(size,color):
    t.pendown()
    t.setheading(0)
    t.color(color)
    t.fillcolor(color)
    t.begin_fill()
    for i in range(3):
        t.fd(size)
        t.lt(120)
    t.end_fill()
    t.penup() 
    
